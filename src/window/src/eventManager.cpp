#include "../hdr/eventManager.h"

int EventManager::processEvents(GLFWwindow *win)
{
    up = glfwGetKey(win, GLFW_KEY_UP);
    down = glfwGetKey(win, GLFW_KEY_DOWN);

    if (up == GLFW_PRESS)
    {
        xangle += 0.0;
        yangle += 0.5;

        return 1;
    };

    if (down == GLFW_PRESS)
    {
        xangle += 0.0;
        yangle += -0.5;

        return 1;
    };

    return 1;
};